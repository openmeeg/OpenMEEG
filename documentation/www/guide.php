<div class="part_title">
    Implementation and details for developpers
</div>

<div class="part_content">
    <ul>
        <li> Multiplatform compilation using <a href="http://www.cmake.org/">CMake</a> (Tested on Mac OS X, Windows XP, Linux 32 and 64). </li>
        <li> Test procedures using CTest provided with <a href="http://www.cmake.org/">CMake</a> (Tests run on Mac OS X, Windows XP, Linux 32 and 64). </li>
        <li> OpenMP and/or MKL (license needed for developpers) to accelerate processing on multicore architectures (requires gcc >= 4.2 or icc). </li>
        <li> Handled through the <a href="https://gforge.inria.fr/projects/openmeeg/">the INRIA forge</a>. </li>
        <li> Source versioning using <a href="http://subversion.apache.org/">Subversion (svn)</a>. Check <a href="https://gforge.inria.fr/scm/?group_id=435">the gforge SCM page</a>. </li>
        <li> <a href="http://cdash.inria.fr/CDash/index.php?project=openMEEG">Dashboard</a> to check status of various platforms (svn trunk sources). </li>
    </ul>
</div>
